//题目：两个字符串连接程序   怎么突然这么简单
public class test46 {
/*    这个程序定义了两个字符串 str1 和 str2，然后使用 + 运算符将它们连接起来，结果存储在 result 变量中。
最后，程序打印出连接后的字符串。*/
        public static void main(String[] args) {
            String str1 = "Hello, ";
            String str2 = "World!";

            String result = str1 + str2;

            System.out.println("连接后的字符串: " + result);
        }

}
